age = 25
temperature = 72
price = 19.99
username = "Gab"

# ==
print(f"Is age equal to 25? {age == 25}")
print(f"Is age equal to 30? {age == 30}")

# !=
print(f"\nIs temperature not 70? {temperature != 70}")
print(f"\nIs username not 'Alex'? {username != 'Alex'}")

# >
print(f"\nIs age greater than 18? {age > 18}")
print(f"Is age greater than 30? {age > 30}")

# <
print(f"\nIs price less than 20? {price < 20}")
print(f"Is price less than 10? {price < 10}")

# >=
print(f"\nIs age >= 25? {age >= 25}")
print(f"Is age >= 26? {age >= 26}")

# <=
print(f"\nIs temperature <= 72? {temperature <= 72}")
print(f"Is temperature <= 71? {temperature <= 71}")

# == vs. =
print("\n= is for ASSIGNMENT (setting a value)")
print("== is for COMPARISON (checking if equal)")